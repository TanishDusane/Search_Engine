package com.SearchEnginePackage;

// Class representing a search history result
public class HistoryResult {
    // Private fields to store keyword and link information
    private String keyword;
    private String link;

    // Getter method for retrieving the keyword
    public String getKeyword() {
        return keyword;
    }

    // Setter method for setting the keyword
    public void setKeyword(String keyword) {
        this.keyword = keyword;
    }

    // Getter method for retrieving the link
    public String getLink() {
        return link;
    }

    // Setter method for setting the link
    public void setLink(String link) {
        this.link = link;
    }
}