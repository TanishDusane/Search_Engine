package com.SearchEnginePackage;

// Class representing a search result
public class SearchResult {
    // Private fields to store title and link information
    private String title;
    private String link;

    // Getter method for retrieving the title
    public String getTitle() {
        return title;
    }

    // Setter method for setting the title
    public void setTitle(String title) {
        this.title = title;
    }

    // Getter method for retrieving the link
    public String getLink() {
        return link;
    }

    // Setter method for setting the link
    public void setLink(String link) {
        this.link = link;
    }
}